package cmd

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
	"github.com/spf13/viper"
)

var cfgFile string

var rootCmd = &cobra.Command{
	Use:   "go-lastfm-to-twitter",
	Short: "Get your last.fm chart and publish it on twitter",
	Long:  `Publish your current last.fm chart into your twitter account`,
	// Uncomment the following line if your bare application
	// has an action associated with it:
	//	Run: func(cmd *cobra.Command, args []string) { },
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

// Config ...
type Config struct {
	Twitter struct {
		Consumer    string
		Secret      string
		Token       string
		TokenSecret string
	}
}

func init() {
	cobra.OnInitialize(initConfig)

	cfg := &Config{}
	rootCmd.PersistentFlags().StringVar(&cfgFile, "config", ".config.yaml", "config file")
	rootCmd.PersistentFlags().StringVar(&cfg.Twitter.Consumer, "twitter-consumer", "", "twitter consumer key")
}

// initConfig reads in config file and ENV variables if set.
func initConfig() {
	if cfgFile != "" {
		viper.SetConfigFile(cfgFile)
	} else {
		viper.AddConfigPath("./")
		viper.SetConfigName(".config.yaml")
	}

	viper.AutomaticEnv()
	if err := viper.ReadInConfig(); err == nil {
		fmt.Println("Using config file:", viper.ConfigFileUsed())
	}
}
